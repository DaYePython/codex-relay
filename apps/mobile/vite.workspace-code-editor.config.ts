import { writeFileSync } from "node:fs";
import type { Plugin } from "vite";
import { defineConfig } from "vite";
import { viteSingleFile } from "vite-plugin-singlefile";

const stringifyHtml = (output: string): Plugin => ({
  name: "workspace-code-editor-html-stringify",
  enforce: "post",
  generateBundle(_, bundle) {
    for (const [fileName, assetInfo] of Object.entries(bundle)) {
      if (
        fileName.endsWith(".html") &&
        assetInfo &&
        typeof assetInfo === "object" &&
        "source" in assetInfo &&
        typeof assetInfo.source === "string"
      ) {
        writeFileSync(
          output,
          [
            "/**",
            " * This file is generated by the workspace code editor build.",
            " * Do not edit this file directly.",
            " */",
            `export default ${JSON.stringify(assetInfo.source)}`,
          ].join("\n"),
        );
      }
    }
  },
});

export default defineConfig({
  build: {
    emptyOutDir: true,
    outDir: "../../dist/workspace-code-editor",
    target: "es2020",
  },
  plugins: [
    viteSingleFile({
      deleteInlinedFiles: true,
      removeViteModuleLoader: true,
    }),
    stringifyHtml("./src/components/chat/workspace-preview/workspace-code-editor-html.ts"),
  ],
  publicDir: false,
  root: "./web/workspace-code-editor",
});
